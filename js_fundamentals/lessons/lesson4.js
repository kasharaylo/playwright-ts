// Relational or comparison operators

// >  - more than
// < - less than
// >= - more than equal
// <= - less than equal

var result = 10 > 5
console.log(result) // true

var result2 = 10 < 5
console.log(result2) // false

var result3 = 5 >= 5
console.log(result3) // true

var result4 = 10 <= 5 // false
console.log(result4)

// Equality Operators

// = - equal

var x = 10
console.log(x == '10') // true // lose comparison (WITHOUT data type)
console.log(x === '10') // false // strict comparison (WITH data type)